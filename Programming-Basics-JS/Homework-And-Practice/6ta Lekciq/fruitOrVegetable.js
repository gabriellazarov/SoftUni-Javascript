function check(arg) {
    let thing = arg;
    if (thing == "banana" || thing == "apple" || thing == "kiwi" || thing == "cherry" || thing == "lemon" || thing == "grapes") console.log("fruit");
    else if (thing == "tomato" || thing == "cucumber" || thing == "pepper" || thing == "carrot") console.log("vegetable");
    else console.log("unknown");
}